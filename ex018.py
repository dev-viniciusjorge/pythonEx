import math
ang = float(input('Digite o ângulo que você deseja: '))
print('O angulo de {} tem o seno de {:.2f}.'.format(ang, math.sin(math.radians(ang))))
print('O angulo de {} tem o seno de {:.2f}.'.format(ang, math.cos(math.radians(ang))))
print('O angulo de {} tem o seno de {:.2f}. '.format(ang, math.tan(math.radians(ang))))
